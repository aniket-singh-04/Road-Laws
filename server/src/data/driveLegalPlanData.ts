const squarePolygon = (lat: number, lng: number, delta = 0.08) => ({
  type: 'Polygon',
  coordinates: [[
    [lng - delta, lat - delta],
    [lng + delta, lat - delta],
    [lng + delta, lat + delta],
    [lng - delta, lat + delta],
    [lng - delta, lat - delta],
  ]],
})

